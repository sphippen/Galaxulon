{
	assert2(t != 0, "Passed null parameter to path function");

    if(*t < 60)
    {
        return Vec2(0, 1);
    }
    else if(*t < 120)
    {
        return Vec2(0, -1);
    }
    else
    {
        *t = 0;
        return Vec2(0, 1);
    }
}

