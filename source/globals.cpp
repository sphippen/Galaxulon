void g_firePowerPowerDown(Player* player)
{
	assert2(baseLightDamage > 0, "The player's base light damage wasn't positive or wasn't upgraded.");
	assert2(baseHeavyDamage > 0, "The player's base heavy damage wasn't positive or wasn't upgraded.");
	player->setLightDamage(baseLightDamage);
	player->setHeavyDamage(baseHeavyDamage);
	baseLightDamage = -1;
	baseHeavyDamage = -1;
	player->setPoweredUpFire(false);
}

